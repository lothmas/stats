package com.material.trending;

public class Trending {
    private String profilePic;
    private String title;
    private String owner;
    private String description;
    private int descriptionType;
    private String time;
    private String mainDisplay;
    private int voteType;
    private int voteBy;
    private int voteId;



    public String getProfilePic() {
        return profilePic;
    }

    public void setProfilePic(String profilePic) {
        this.profilePic = profilePic;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getDescriptionType() {
        return descriptionType;
    }

    public void setDescriptionType(int descriptionType) {
        this.descriptionType = descriptionType;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMainDisplay() {
        return mainDisplay;
    }

    public void setMainDisplay(String mainDisplay) {
        this.mainDisplay = mainDisplay;
    }

    public int getVoteType() {
        return voteType;
    }

    public void setVoteType(int voteType) {
        this.voteType = voteType;
    }

    public int getVoteBy() {
        return voteBy;
    }

    public void setVoteBy(int voteBy) {
        this.voteBy = voteBy;
    }

    public int getVoteId() {
        return voteId;
    }

    public void setVoteId(int voteId) {
        this.voteId = voteId;
    }
}
